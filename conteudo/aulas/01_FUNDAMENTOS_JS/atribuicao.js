const a = 7
let b = 3

b += a // Atribuiçao aditiva // Seria equivalente a  B=B + A
console.log(b)

b -= 4 // Atribuição subtrativa // Seria equivalente a  B=B - 4
console.log(b)

b *= 2 // Atribuição multiplicativa // Seria equivalente a  B=B x 4
console.log(b)

b /= 2 // Atribuição divisiva // Seria equivalente a  B=B / 2
console.log(b)

b %= 2 // Atribuição modular // Seria equivalente a  B=B % 2
console.log(b)